var isUnique = function( str ) {
  var checker = 0;
  for(var index = 0; index < str.length; index++) {
    var diff = str.charCodeAt(index) - str.charCodeAt('a');
    if( (checker & (1 << diff) ) > 0 ) {
      return false
    }
    checker = checker | (1<<diff);
  }
  return true
  
}

